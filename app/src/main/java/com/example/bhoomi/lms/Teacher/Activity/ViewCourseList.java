package com.example.bhoomi.lms.Teacher.Activity;

public class ViewCourseList {

    private String course_image;
    private String category_name;
    private String subcategory_name;
    private String course_price;
    private String course_new_price;

    public String getCourse_image() {
        return course_image;
    }

    public void setCourse_image(String course_image) {
        this.course_image = course_image;
    }

    public String getCategory_name() {
        return category_name;
    }
    public void setCategory_name(String category_name) {
        this.category_name = category_name;
    }

    public String getSubcategory_name() {
        return subcategory_name;
    }
    public void setSubcategory_name(String subcategory_name) {
        this.subcategory_name = subcategory_name;
    }



    public String getCourse_new_price() {
        return course_new_price;
    }
    public void setCourse_new_price(String course_new_price) {
        this.course_new_price = course_new_price;
    }

    public String getCourse_price() {
        return course_price;
    }
    public void setCourse_price(String course_price) {
        this.course_price = course_price;
    }



}
