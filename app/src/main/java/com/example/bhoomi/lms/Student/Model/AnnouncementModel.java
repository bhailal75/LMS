package com.example.bhoomi.lms.Student.Model;

public class AnnouncementModel {

    private String username, que, ans, numofResponse, lec_num, time;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getQue() {
        return que;
    }

    public void setQue(String que) {
        this.que = que;
    }

    public String getAns() {
        return ans;
    }

    public void setAns(String ans) {
        this.ans = ans;
    }

    public String getNumofResponse() {
        return numofResponse;
    }

    public void setNumofResponse(String numofResponse) {
        this.numofResponse = numofResponse;
    }

    public String getLec_num() {
        return lec_num;
    }

    public void setLec_num(String lec_num) {
        this.lec_num = lec_num;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
