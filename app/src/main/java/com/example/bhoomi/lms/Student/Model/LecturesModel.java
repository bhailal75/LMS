package com.example.bhoomi.lms.Student.Model;

public class LecturesModel {

    private  int lec_num;
    private String lec_title, lec_duration;

    public int getLec_num() {
        return lec_num;
    }

    public void setLec_num(int lec_num) {
        this.lec_num = lec_num;
    }

    public String getLec_title() {
        return lec_title;
    }

    public void setLec_title(String lec_title) {
        this.lec_title = lec_title;
    }

    public String getLec_duration() {
        return lec_duration;
    }

    public void setLec_duration(String lec_duration) {
        this.lec_duration = lec_duration;
    }
}
